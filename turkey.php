        <h5>Приобретение собственности иностранцами</h5>
        <p>Закон о приобретении недвижимости в Турции предусматривает покупку, продажу и наследование недвижимости для граждан Казахстана и других стран. Процедура оформления занимает, приблизительно от 2 до 5 месяцев. Процесс покупки недвижимости в Турции для всех иностранных граждан одинаков. Согласно ст. 35 Закона о собственности №2644 от ноября 1934 с изменениями и дополнениями от июля 2003, «иностранные физические лица и торговые компании», образующие юридическое лицо, а также созданные за рубежом в соответствии с законодательством своей страны – обладают правом приобретать недвижимое имущество, находящееся в пределах границ Турецкой Республики на основе принципа взаимности. Это означает, что турецкую недвижимость имеют право покупать граждане тех государств, законодательство которых позволяют приобретать недвижимость гражданам Турции. Это условие не является обязательным для лиц без гражданства, беженцев и лиц, осуществляющих инвестирование в сферу туризма Турции.</p>
        <p>Изменения в законе для иностранцев, покупка недвижимости для физических лиц.</p>
        <p>7 января 2006 в официальной турецкой прессе опубликован новый закон №5444, позволяющий иностранным физическим лицам приобретать недвижимое имущество на территории страны. Основные изменения по сравнению с предыдущим законом касаются продажи земельных участков в Турции. Теперь максимальная площадь участка, который может быть продан иностранцу или фирме с участием иностранного капитала, ограничена 25 тыс. кв. м. Также изменена и существенно упрощена сама процедура оформления покупки недвижимости в Турции. Разрешение на покупку недвижимости иностранцам будет выдаваться непосредственно в Кадастровом управлении и одновременно оформляться TAPU.</p>
